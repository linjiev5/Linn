package c008;

public class TTTT {

	public static void main(String[] args) {
		//
		int i = 10, j = 10;

		Integer m = 10, n = 10;

		Integer q = 500, w = 500;

		System.out.println(i == j);
		//		⇒true
		System.out.println(i == m);
		//		⇒flase
		System.out.println(m == n);
		//		⇒flase
		System.out.println(q == w);
		//		⇒flase
	}

}
